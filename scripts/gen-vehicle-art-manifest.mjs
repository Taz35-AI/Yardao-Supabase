// scripts/gen-vehicle-art-manifest.mjs
// Scans public/Vehicles/ and regenerates src/lib/vehicleArt/manifest.ts.
//
// Run automatically via `prebuild`/`predev` (see package.json), or manually:
//   node scripts/gen-vehicle-art-manifest.mjs
//
// Dropping a new `make+model.svg` into public/Vehicles/ and rebuilding is all
// that's needed to link a new model — no code edits.
import { readdirSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const artDir = join(root, 'public', 'Vehicles')
const outFile = join(root, 'src', 'lib', 'vehicleArt', 'manifest.ts')

// Files that are not make/model vehicle art.
// (Underscore-prefixed files are reserved, e.g. _generic.svg fallback.)
const EXCLUDE = new Set(['file.svg'])

const basenames = readdirSync(artDir)
  .filter(
    (f) =>
      f.toLowerCase().endsWith('.svg') &&
      !f.startsWith('_') &&
      !EXCLUDE.has(f.toLowerCase()),
  )
  .map((f) => f.slice(0, -4)) // strip ".svg"
  .sort((a, b) => a.localeCompare(b))

const banner = `// AUTO-GENERATED by scripts/gen-vehicle-art-manifest.mjs — do not edit by hand.
// Run \`node scripts/gen-vehicle-art-manifest.mjs\` (or rebuild) to refresh.
`

const body =
  banner +
  `export const VEHICLE_ART_FILES: string[] = [\n` +
  basenames.map((b) => `  ${JSON.stringify(b)},`).join('\n') +
  `\n]\n`

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, body, 'utf8')

console.log(`vehicle-art manifest: ${basenames.length} files -> ${outFile}`)
